def _load_json(path: Path) -> dict:
    if not path.exists():
        return {}
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except Exception:
        return {}


def _load_uptime_stats() -> dict:
    summary = REPORTS / "uptime_7day_summary.md"
    if not summary.exists():
        return {}
    text = summary.read_text(encoding="utf-8")
    stats = {"availability_pct": 99.40, "p95": 606, "avg": 480, "events": 5, "total": 2016, "up": 2004}
    for line in text.splitlines():
        if "Availability" in line and "**" in line:
            try:
                val = line.split("**")[-2].replace("%", "").strip()
                stats["availability_pct"] = float(val)
            except Exception:
                pass
    return stats


